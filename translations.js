function applyTranslations(lang) {
    document.querySelectorAll('[data-i18n]').forEach(el => {
        const key = el.getAttribute('data-i18n');
        if (translations[lang][key]) {
            el.innerHTML = translations[lang][key];
        }
    });

    document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
        const key = el.getAttribute('data-i18n-placeholder');
        if (translations[lang][key]) {
            el.placeholder = translations[lang][key];
        }
    });

    // Update active state of language buttons
    document.querySelectorAll('.lang-btn').forEach(btn => {
        btn.classList.remove('active');
        if (btn.id === `lang-${lang}`) btn.classList.add('active');
    });

    localStorage.setItem('site-lang', lang);
}

document.addEventListener('DOMContentLoaded', () => {
    const savedLang = localStorage.getItem('site-lang') || 'ko';
    
    // Bind click events
    const koBtn = document.getElementById('lang-ko');
    const enBtn = document.getElementById('lang-en');

    if (koBtn) {
        koBtn.onclick = (e) => { e.preventDefault(); applyTranslations('ko'); };
    }
    if (enBtn) {
        enBtn.onclick = (e) => { e.preventDefault(); applyTranslations('en'); };
    }

    // --- Common Search Logic ---
    const searchBtn = document.querySelector('.search-btn');
    const searchOverlay = document.getElementById('search-overlay');
    const searchCloseBtn = document.getElementById('search-close-btn');
    const searchInput = document.getElementById('search-input');
    const searchResults = document.createElement('div');
    searchResults.id = 'search-results';
    searchResults.className = 'search-results';
    if (searchOverlay && !document.getElementById('search-results')) {
        searchOverlay.querySelector('.search-container').appendChild(searchResults);
    }

    const searchableData = [
        { ko: "기업소개", en: "About Us", url: "index.html#about", tags: ["회사", "소개", "정보"] },
        { ko: "핵심솔루션", en: "Solutions", url: "index.html#services", tags: ["기술", "제품", "서비스"] },
        { ko: "기술진", en: "Experts", url: "index.html#faculty", tags: ["인재", "교수", "진"] },
        { ko: "기술지원", en: "Tech Support", url: "index.html#resources", tags: ["백서", "가이드", "자료"] },
        { ko: "기업소식", en: "News", url: "index.html#news", tags: ["공지", "이벤트", "소식"] },
        { ko: "포럼", en: "Forum", url: "board.html", tags: ["게시판", "문의", "글쓰기"] },
        { ko: "문의하기", en: "Contact Us", url: "contact.html", tags: ["이메일", "전화", "상담", "문의"] },
        { ko: "경쟁력 리포트", en: "Competitiveness Report", url: "report.html", tags: ["성과", "지표", "성장"] }
    ];

    if (searchBtn && searchOverlay) {
        searchBtn.onclick = (e) => {
            e.preventDefault();
            searchOverlay.classList.add('active');
            document.body.style.overflow = 'hidden';
            setTimeout(() => {
                if (searchInput) searchInput.focus();
            }, 500);
        };
    }

    if (searchInput) {
        searchInput.oninput = (e) => {
            const val = e.target.value.trim().toLowerCase();
            const lang = localStorage.getItem('site-lang') || 'ko';
            const quickLinks = searchOverlay.querySelector('.search-quick-links');
            
            if (val.length > 0) {
                quickLinks.style.display = 'none';
                searchResults.style.display = 'block';
                
                const matches = searchableData.filter(item => {
                    const text = item[lang].toLowerCase();
                    const tagsMatch = item.tags.some(tag => tag.toLowerCase().includes(val));
                    return text.includes(val) || tagsMatch;
                });

                if (matches.length > 0) {
                    searchResults.innerHTML = matches.map(item => `
                        <div class="result-item">
                            <a href="${item.url}">
                                <span class="result-cat">${lang === 'ko' ? '페이지' : 'Page'}</span>
                                <span class="result-title">${item[lang]}</span>
                                <span class="result-arrow">→</span>
                            </a>
                        </div>
                    `).join('');
                } else {
                    searchResults.innerHTML = `<div class="no-results">${lang === 'ko' ? '검색 결과가 없습니다.' : 'No results found.'}</div>`;
                }
            } else {
                quickLinks.style.display = 'block';
                searchResults.style.display = 'none';
                searchResults.innerHTML = '';
            }
        };
    }

    if (searchCloseBtn && searchOverlay) {
        searchCloseBtn.onclick = () => {
            searchOverlay.classList.remove('active');
            document.body.style.overflow = 'auto';
            if (searchInput) searchInput.value = '';
            searchOverlay.querySelector('.search-quick-links').style.display = 'block';
            searchResults.style.display = 'none';
        };
    }

    // Close search on Esc key
    window.addEventListener('keydown', (e) => {
        if (e.key === 'Escape' && searchOverlay && searchOverlay.classList.contains('active')) {
            searchOverlay.classList.remove('active');
            document.body.style.overflow = 'auto';
        }
    });

    applyTranslations(savedLang);
});
